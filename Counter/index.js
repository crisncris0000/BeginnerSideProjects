document.querySelector("#Increase").onclick = function()
{
    
    let h1 = document.querySelector("#Counter");
    h1.innerHTML ++;

}

document.querySelector("#Decrease").onclick = function()
{
    
    let h1 = document.querySelector("#Counter");
    h1.innerHTML --;

}

document.querySelector("#Reset").onclick = function()
{
    
    let h1 = document.querySelector("#Counter");
    h1.innerHTML = 0;

}



